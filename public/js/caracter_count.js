$(document).on("input", ".contador1", function () {
   
    var limite = 500;
    var caracteresDigitados = $(this).val().length;
    var caracteresRestantes = limite - caracteresDigitados;

    $(".caracteres1").text(caracteresRestantes + ' caracteres restantes');
});

